public class P6Expressions {
        public static void main (String[] args) {
            int friends;
            friends = 10;
            friends = friends + 1;      //make new friend
            friends = friends - 1;      //lost a friends
            friends = friends * 2;      //double my friends
            friends = friends / 2;      //half of friends
            friends = friends % 2;      //remainder of devision by 2
            friends = 10;               //10 friends
            
            friends++;                  //make new friend
            friends--;                  //lost a friends

            friends += 5;
            friends = friends + 5;

            System.out.println(friends);

            

        }
    
}
